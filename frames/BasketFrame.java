   // Build Table Model on Result Set of database
    public static DefaultTableModel buildTableModel(ResultSet rs) throws SQLException {
        ResultSetMetaData metaData = rs.getMetaData();
        Vector<String> columnNames = new Vector<>();
        int columnCount = metaData.getColumnCount();
        for (int column = 1; column <= columnCount; column++) {
            columnNames.add(metaData.getColumnName(column));
        }
        Vector<Vector<Object>> data = new Vector<Vector<Object>>();
        while (rs.next()) {
            Vector<Object> vector = new Vector<Object>();
            for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
                vector.add(rs.getObject(columnIndex));
            }
            data.add(vector);
        }
        return new DefaultTableModel(data, columnNames);
    }
    // Populate Table
    private void populateTable() throws SQLException {
        BasketItemDao basketItemDao = new BasketItemDaoImpl();
        basketItemTbl.setModel(buildTableModel(basketItemDao.getAllBasketItems()));
    }
    
    // Validate empty fields
    public Boolean validateFields() {
        Boolean isValid = true;
        if(basketIdTxt.getText().equals("") || basketIdTxt.getText().equals(null)){
            basketIdLbl.setText("*Empty Field");
            isValid = false;
        }
        if(productIdTxt.getText().equals("") || productIdTxt.getText().equals(null)){
            productIdLbl.setText("*Empty Field");
            isValid = false;
        }
        if(quantityTxt.getText().equals("") || quantityTxt.getText().equals(null)){
            quantityLbl.setText("*Empty Field");
            isValid = false;
        }
        if(priceTxt.getText().equals("") || priceTxt.getText().equals(null)){
            priceLbl.setText("*Empty Field");
            isValid = false;
        }
        return isValid;
    }
    
