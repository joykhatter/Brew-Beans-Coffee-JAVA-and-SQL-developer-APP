    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if(validateFields()) {
            BasketItemBean basketItemBean = new BasketItemBean();
            basketItemBean.setBasketId(Integer.parseInt(basketIdTxt.getText()));
            basketItemBean.setProductId(Integer.parseInt(productIdTxt.getText()));
            basketItemBean.setPrice(Double.parseDouble(priceTxt.getText()));
            basketItemBean.setQuantity(Integer.parseInt(quantityTxt.getText()));
            basketItemBean.setSizeCode(Integer.parseInt((String)sizeCodeTxt.getSelectedItem()));
            basketItemBean.setFormCode(Integer.parseInt((String)formCodeTxt.getSelectedItem()));
            BasketItemDao basketItemDao = new BasketItemDaoImpl();
            if(basketItemDao.addBasketItem(basketItemBean)){
                JOptionPane.showMessageDialog(this, "Item Added Successfully");
                try {
                    populateTable();
                } catch (SQLException ex) {
                    Logger.getLogger(BasketFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                clearFields();
                
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed
