    // Add Button Action Listener
    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if(validateFields()) {
            BasketStatusBean basketStatusBean = new BasketStatusBean();
            basketStatusBean.setBasketID(Integer.parseInt(basketNoTxt.getText()));
            basketStatusBean.setDate(new java.util.Date(dateShippedTxt.getText()));
            basketStatusBean.setShipper(shipperTxt.getText());
            basketStatusBean.setShippingNum(shippingNoTxt.getText());
            BasketStatusDao basketStatusDao = new BasketStatusDaoImpl();
            Boolean added = basketStatusDao.updateBasketStatus(basketStatusBean);
            if(added) {
                JOptionPane.showMessageDialog(this, "Basket Status Updated Successfully");
                try {
                    populateTable();
                    clearFields();
                } catch (SQLException ex) {
                    Logger.getLogger(BasketStatusFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed
