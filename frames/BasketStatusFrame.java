   // Build Table Model on Result Set of Database query
    public static DefaultTableModel buildTableModel(ResultSet rs) throws SQLException {
        ResultSetMetaData metaData = rs.getMetaData();
        Vector<String> columnNames = new Vector<>();
        int columnCount = metaData.getColumnCount();
        for (int column = 1; column <= columnCount; column++) {
            columnNames.add(metaData.getColumnName(column));
        }
        Vector<Vector<Object>> data = new Vector<Vector<Object>>();
        while (rs.next()) {
            Vector<Object> vector = new Vector<Object>();
            for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
                vector.add(rs.getObject(columnIndex));
            }
            data.add(vector);
        }
        return new DefaultTableModel(data, columnNames);
    }
    // Populate Table
    private void populateTable() throws SQLException {
        BasketStatusDao basketStatusDao = new BasketStatusDaoImpl();
        orderStatusTable.setModel(buildTableModel(basketStatusDao.getAllBasketStatus()));
    }
    
    // Check Empty Fields
    public Boolean validateFields() {
        Boolean isValid = true;
        if(basketNoTxt.getText().equals("") || basketNoTxt.getText().equals(null)) {
            isValid = false;
            basketNoLbl.setText("*Empty Field");
        }
        if(dateShippedTxt.getText().equals("") || dateShippedTxt.getText().equals(null)) {
            isValid = false;
            dateShippedLbl.setText("*Empty Field");
        }
        if(shipperTxt.getText().equals("") || shipperTxt.getText().equals(null)) {
            isValid = false;
            shipperLbl.setText("*Empty Field");
        }
        if(shippingNoTxt.getText().equals("") || shippingNoTxt.getText().equals(null)) {
            isValid = false;
            shippingNumLbl.setText("*Empty Field");
        }
        
        return isValid;
    }
    
